function numberType() {
    let n = 12
    if(n>0) {
          console.log("Number is greater than 0:",n)
    }else if(n<0) {
          console.log("Number is lesser than 0:",n)
    }else if(n===0) {
          console.log("Number is zero:",n)
    }else {
          console.log("Given value is not number")
    }
}
numberType()